package com.example.Library.entity;

import org.springframework.security.core.GrantedAuthority;

import java.util.Collection;

/** Перечисление ролей пользователей в системе.
 * <p>
 * Определяет возможные роли, которые могут быть назначены пользователям.
 * Используется для управления правами доступа и авторизацией.
 * </p>
 *
 * @see UserEntity Класс пользователя, который использует эти роли */
public enum ROLE {
    /** Роль обычного пользователя.
     * <p>
     * Предоставляет базовые права:
     * - Просмотр книг
     * - Бронирование книг
     * - Просмотр своих бронирований
     * </p> */
    USER,
    /** Роль администратора системы.
     * <p>
     * Предоставляет расширенные права:
     * - Управление книгами (добавление/редактирование/удаление)
     * - Просмотр всех бронирований
     * - Управление пользователями
     * </p> */
    ADMIN;
}
